CODE_ENTRY(defaultFuncCommon)
	mflr	r0				// save link register
	std	r0,STK_LR(r1)
	std	r3,STK_PARAM0(r1)		// save parameter gprs
	std	r4,STK_PARAM1(r1)
	std	r5,STK_PARAM2(r1)
	std	r6,STK_PARAM3(r1)
	std	r7,STK_PARAM4(r1)
	std	r8,STK_PARAM5(r1)
	std	r9,STK_PARAM6(r1)
	std	r10,STK_PARAM7(r1)
	stfd	f1,((-1)*8)(r1)			// save parameter fprs
	stfd	f2,((-2)*8)(r1)
	stfd	f3,((-3)*8)(r1)
	stfd	f4,((-4)*8)(r1)
	stfd	f5,((-5)*8)(r1)
	stfd	f6,((-6)*8)(r1)
	stfd	f7,((-7)*8)(r1)
	stfd	f8,((-8)*8)(r1)
	stfd	f9,((-9)*8)(r1)
	stfd	f10,((-10)*8)(r1)
	stfd	f11,((-11)*8)(r1)
	stfd	f12,((-12)*8)(r1)
	stfd	f13,((-13)*8)(r1)
	la	r3,STK_PARAM0(r1)		// first param:  &this
	mr	r4,r12				// second param:  method number
	stdu	r1,-(STK_SIZE+(13*8))(r1)	// push frame
	bl	C_TEXT(GenericDefaultFunc)	// call generic function
	la	r1,(STK_SIZE+(13*8))(r1)	// pop frame
	ld	r0,STK_LR(r1)			// restore link register
	mtlr	r0                             
	cmpldi	r3,0				// check return value
	bne     OnSuccess                       
	SERROR(r3,1223,0xFF,0)                  // if zero then error
	blr   	                                // return to caller
OnSuccess:		
	ld	r2,8(r3)			// load toc from func desc
	ld	r3,0(r3)			// load ctr from func desc
	mtctr	r3
	ld	r3,STK_PARAM0(r1)		// restore parameter gprs
	ld	r4,STK_PARAM1(r1)
	ld	r5,STK_PARAM2(r1)
	ld	r6,STK_PARAM3(r1)
	ld	r7,STK_PARAM4(r1)
	ld	r8,STK_PARAM5(r1)
	ld	r9,STK_PARAM6(r1)
	ld	r10,STK_PARAM7(r1)
	lfd	f1,((-1)*8)(r1)			// restore parameter fprs
	lfd	f2,((-2)*8)(r1)
	lfd	f3,((-3)*8)(r1)
	lfd	f4,((-4)*8)(r1)
	lfd	f5,((-5)*8)(r1)
	lfd	f6,((-6)*8)(r1)
	lfd	f7,((-7)*8)(r1)
	lfd	f8,((-8)*8)(r1)
	lfd	f9,((-9)*8)(r1)
	lfd	f10,((-10)*8)(r1)
	lfd	f11,((-11)*8)(r1)
	lfd	f12,((-12)*8)(r1)
	lfd	f13,((-13)*8)(r1)
	bctr					// branch to method
CODE_END(defaultFuncCommon)

